package com.shinav.mathapp.db.pojo;

public class GivenApproach {

    private String key;
    private String approachKey;
    private String arrangement;
    private int givenAt;

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getApproachKey() {
        return approachKey;
    }

    public void setApproachKey(String approachKey) {
        this.approachKey = approachKey;
    }

    public String getArrangement() {
        return arrangement;
    }

    public void setArrangement(String arrangement) {
        this.arrangement = arrangement;
    }

    public int getGivenAt() {
        return givenAt;
    }

    public void setGivenAt(int givenAt) {
        this.givenAt = givenAt;
    }

}
